  final double weight;
  final int systolic;
  final int diastolic;
  final double sugar;
  final double kidney;

  PatientData({
    required this.date,
    required this.weight,
    required this.systolic,
    required this.diastolic,
    required this.sugar,
    required this.kidney,
  });
}
